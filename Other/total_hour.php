<h1>活動目的</h1>
    <ul>
    「教育部數位學習服務平台」(底下簡稱UPS)提供多元、自主及開放的學習環境，以推廣終身學習目標，促進知識交流與分享，
    為鼓勵教育部數位學習服務平台會員多多參與本部建置於「數位學習服務平台」內之優良課程，
    平台舉辦「呼朋引伴！有加有保佑」活動，希望能讓更多的群眾能在數位學習服務平台一同參與及學習。
    </ul>

